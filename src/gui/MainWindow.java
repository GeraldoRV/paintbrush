    private void changeColorBackground(String color) {
        switch (color) {
            case "Red":
                panel.setBackground(Color.red);
                break;
            case "Yellow":
                panel.setBackground(Color.yellow);
                break;
            case "Blue":
                panel.setBackground(Color.blue);
                break;
            default:
                panel.setBackground(Color.white);
        }

    }

