    private void changeColorPaintBrush(String color) {
        switch (color) {
            case "Green":
                graphics.setColor(Color.green);
                break;
            case "Black":
                graphics.setColor(Color.black);
                break;
            case "Orange":
                graphics.setColor(Color.orange);
                break;
            default:
        }

    }
